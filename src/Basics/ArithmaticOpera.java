package Basics;

public class ArithmaticOpera {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

        double p = 5;
        double q = 2;
        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
    }
}
